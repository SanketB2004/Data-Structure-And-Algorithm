package Pattern_Printing;

/**
 * ractangle
 */
public class ractangle {

    public static void main(String[] args) {
        
        System.out.println("Ractangle Pattern");

        for (int i = 1; i <= 5 ; i++) { // row
            

            for (int j = 1; j <= 5; j++) { // col
                
                System.out.print(" * ");
            }

             System.out.println();
        }
        
        
    }
}