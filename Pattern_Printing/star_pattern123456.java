package Pattern_Printing;

public class star_pattern123456 {
   

/**
 * star_Trangle
 */


    public static void main(String[] args) {
        
        
        System.out.println("Star  Pattern");
        int c = 1;

        int a = 5;

        for (int i = 0; i <= a ; i++) { // row
            

            for (int j = 0; j <= i; j++) { // col
                
                System.out.print(c++ +" ");
            }

             System.out.println();
        }
    }
}
